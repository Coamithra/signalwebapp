// Regenerates public/emoji-shortcodes.js from Signal Desktop's own emoji table.
//
// Signal ships build/emoji-data.json inside its app.asar: { emojis: { "🤷": {
// shortName: "shrug", skinTones: {…} }, … } }. Reading it (instead of vendoring
// emoji-datasource, or hand-rolling a list) means our ":shortcode:" set is
// exactly the one Signal itself accepts — the same `:shrug:` you'd type in the
// real client. Keeps the app zero-dependency: the output is a plain JS module,
// checked in, so the running app never touches the asar.
//
//   node scripts/gen-emoji-map.mjs            # auto-detect the Signal install
//   node scripts/gen-emoji-map.mjs <app.asar> # or point at one
//
// Re-run after a Signal update to pick up new emoji.

import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import path from 'node:path';

const DEFAULT_ASARS = [
  process.env.LOCALAPPDATA && `${process.env.LOCALAPPDATA}/Programs/signal-desktop/resources/app.asar`,
  '/Applications/Signal.app/Contents/Resources/app.asar',
  '/opt/Signal/resources/app.asar',
].filter(Boolean);

// Read one file out of an asar archive (already-loaded buffer). Layout: a
// chromium Pickle header (4B pickle size, 4B header size, 4B object size, 4B
// JSON byte length) then the JSON directory, then the file data. The data
// section starts at 8 + headerSize — NOT 16 + jsonLength, which misses the
// pickle's 4-byte alignment padding and shifts every file by a few bytes.
function readFromAsar(buf, wanted) {
  const jsonLen = buf.readUInt32LE(12);
  const dataOffset = 8 + buf.readUInt32LE(4);
  const header = JSON.parse(buf.toString('utf8', 16, 16 + jsonLen));

  let node = header;
  for (const part of wanted.split('/').filter(Boolean)) {
    node = node.files?.[part];
    if (!node) throw new Error(`${wanted} not found in the archive`);
  }
  const start = dataOffset + Number(node.offset);
  return buf.toString('utf8', start, start + node.size);
}

const asarPath = process.argv[2] || DEFAULT_ASARS.find((p) => existsSync(p));
if (!asarPath) {
  console.error('Could not find Signal Desktop\'s app.asar. Pass its path as an argument.');
  process.exit(1);
}

const asar = readFileSync(asarPath); // a few hundred MB — read it once
const { emojis } = JSON.parse(readFromAsar(asar, '/build/emoji-data.json'));
const signalVersion = JSON.parse(readFromAsar(asar, '/package.json')).version;

// shortName -> emoji. Signal gives one canonical name per emoji; we add a few
// aliases people actually type (Signal's own names for these are unguessable:
// "+1", "-1"). Aliases never overwrite a canonical name.
const ALIASES = {
  thumbsup: '👍', thumbsdown: '👎', thumbs_up: '👍', thumbs_down: '👎',
  laughing: '😆', grinning: '😀', heart: '❤️', check: '✅', x: '❌',
};

const map = {};
for (const [emoji, meta] of Object.entries(emojis)) {
  const name = meta?.shortName;
  if (!name || !/^[a-z0-9_+-]+$/.test(name)) continue;
  if (!(name in map)) map[name] = emoji;
}
for (const [name, emoji] of Object.entries(ALIASES)) {
  if (!(name in map)) map[name] = emoji;
}

const sorted = Object.keys(map).sort();
const body = sorted.map((name) => `  ${/^[a-z][a-z0-9_]*$/.test(name) ? name : JSON.stringify(name)}: ${JSON.stringify(map[name])},`).join('\n');

const out = `// GENERATED by scripts/gen-emoji-map.mjs from Signal Desktop's own
// build/emoji-data.json (Signal ${signalVersion}). Do not edit by hand;
// re-run the script after a Signal update.
//
// ":shortcode:" -> emoji. These are Signal's own shortcodes, so what you type
// here is what you'd type in the real client.

export const EMOJI_SHORTCODES = Object.freeze({
${body}
});
`;

const target = path.resolve(fileURLToPath(new URL('.', import.meta.url)), '../public/emoji-shortcodes.js');
writeFileSync(target, out);
console.log(`wrote ${target} — ${sorted.length} shortcodes from ${asarPath}`);
